<tr>
<td>
<b>Από ποιους συγκαλείται Γενική Συνέλευση;</b><br>
Σύμφωνα με τους Πρότυπου Κανονισμούς η Γενική Συνέλευση συγκαλείται είτε από τη Διαχειριστική Επιτροπή είτε μετά από αίτηση κυρίου μονάδας προς τον Διευθυντή του Κτηματολογίου.<br>
Επίσης μπορεί να συγκληθεί από κύριους μονάδων που είναι ιδιοκτήτες του 25% της κοινόκτητης ιδιοκτησίας, αφού προηγηθεί αίτησή τους προς τη Διαχειριστική Επιτροπή.<br>
<br>
Διαδικασία σύγκλησης Γενικής Συνέλευσης:<br>
<br>
• Δίδεται 7 ημερών ειδοποίηση<br>
• Ορίζεται ο χρόνος και ο τόπος της συνέλευσης<br>
• Εξετάζεται αν υπάρχει απαρτία – δηλαδή θα πρέπει να είναι παρόντες το 50% συν κάτι. Αν δεν υπάρχει τότε αναβάλλεται για μια εβδομάδα και επαναλαμβάνεται την ίδια μέρα και ώρα.<br>
• Εκλέγεται πρόεδρος και γραμματέας της συνέλευσης, ο πρόεδρος συντονίζει και καθοδηγεί, ο δε γραμματέας τηρεί πρακτικά.<br>
• Αν υπάρχει θέμα εκλογής Διαχειριστικής Επιτροπής αποφασίζεται ο αριθμός μελών της<br>
• Υποβάλλονται υποψηφιότητες και εκλέγονται τα μέλη της Διαχειριστικής Επιτροπής<br>
• Οι εκλεγμένοι καταρτίζονται σε σώμα. Εκλέγουν δηλαδή Πρόεδρο, Γραμματέα και Ταμία μεταξύ τους<br>
• Τα πρακτικά της εκλογής της Διαχειριστικής Επιτροπής ή της οποιασδήποτε άλλης απόφασης που λαμβάνεται από την Γενική Συνέλευση καταγράφονται από τον Πρόεδρο και Γραμματέα της Γενικής Συνέλευσης και<br>
καταχωρούνται στο βιβλίο αποφάσεων της οικοδομής και έτσι καθίστανται δεσμευτικά για όλους τους κύριους των μονάδων είτε παρευρίσκονται στην Γενική Συνέλευση είτε όχι, καθώς επίσης και για τους δικαιούχους τους.
