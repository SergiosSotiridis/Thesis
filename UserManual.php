<b>Ασφάλιση Κοινόκτητων Οικοδομών</b><br>
Η ασφάλιση των κοινόκτητων οικοδομών είναι ακόμα μια ειδική πρόνοια που επιβάλλει ο Νόμος. Μετά από πολλές εισηγήσεις και προτάσεις του Συνδέσμου Ασφαλιστών που εισακούστηκαν από το Τμήμα Κτηματολογίο,<br>
αποφασίστηκε όπως ρητά διευκρινιστεί ότι οι κύριοι των μονάδων έχουν το δικαίωμα ξεχωριστής ασφάλισης των μονάδων τους, χωρίς όμως να έχει αρνητικό αποτέλεσμα εάν και όταν επέλθει ο κίνδυνος<br>
στα συμφέροντα της Διαχειριστικής Επιτροπής και ότι δεν επιτρέπει στον ασφαλιστή να καταβάλλει αποχημίωση μικρότερη της αξίας αντικατάστασης, επικαλούμενος υποασφάλιση.<br>
Ταυτόχρονα περιορίζεται το ύψος της αποζημίωσης, ώστε αυτή να μην υπερβαίνει την αγοραία αξία της κοινόκτητης οικοδομής, στην περίπτωση που η αγοραία αξία είναι μικρότερη της αξίας αντικατάστασης.
