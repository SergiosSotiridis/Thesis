<tr>
<td>
<b>Εκλογή Διαχειριστικής Επιτροπής – Εξουσίες και αρμοδιότητες</b><br>
Αφού εκλεγεί ως περιγράφεται ανωτέρω η Διαχειριστική Επιτροπή, θα λειτουργεί ως εντολοδόχος των κυρίων της κοινόκτητης οικοδομής και θα έχει τις εξουσίες που καθορίζονται στο Νόμο.<br>
<br>
Μερικές από τις αρμοδιότητες της Διαχειριστικής Επιτροπής είναι:<br>
<br>
• Να ελέγχει και να λειτουργεί την κοινόκτητη ιδιοκτησία<br>
• Να διατηρεί ταμείο και να καθορίζει, εισπράττει, ακόμα και με αγωγή, τα εισπρακτέα ποσά για τη λειτουργία της οικοδομής<br>
<br>
Ο Νόμος δεν προνοεί για καταχώριση στο Κτηματολόγιο των πρακτικών της Γενικής Συνέλευσης κατά την οποία αποφασίστηκε ο διορισμός Διαχειριστικής Επιτροπής.<br>
<br>
Επομένως η νομιμότητα της επιτροπής αυτής επέρχεται από την απόφαση της Γενικής Συνέλευσης που γνωστοποιείται ενώπιον οποιασδήποτε αρχής με αντίγραφο των πρακτικών της.
</td>
